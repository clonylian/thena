<script setup>
import { ref, onMounted, onUnmounted } from "vue";
let countdown = ref(12 * 60 * 60 * 1000);
let hours = ref(0);
let minutes = ref(0);
let second = ref(0);

let render = () => {
  ms_init();
};
let formatTime = (milliseconds) => {
  hours.value = Math.floor(milliseconds / (1000 * 60 * 60));
  minutes.value = Math.floor((milliseconds % (1000 * 60 * 60)) / (1000 * 60));
  second.value = Math.floor((milliseconds % (1000 * 60)) / 1000);
};
let timer;

let updateCountdown = () => {
  if (countdown.value <= 0) {
    countdown.value = 12 * 60 * 60 * 1000;
  } else {
    countdown.value -= 1000;
  }
  formatTime(countdown.value);
};

onMounted(() => {
  timer = setInterval(updateCountdown, 1000);
});

onUnmounted(() => {
  clearInterval(timer);
});
</script>

